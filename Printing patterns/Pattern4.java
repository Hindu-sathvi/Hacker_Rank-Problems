import java.io.*;
import java.util.*;
class Pattern4
{
	public static void main(String args[])
	{
		System.out.println("increasing triangle with numbers");
		
		  int n=5;
			for(int i=1;i<=n;i++)
			{
			  for(int j=1;j<=i;j++)
			  {
                  System.out.print(j);				  
			  }
            System.out.println();
			}
	
	}			
}
